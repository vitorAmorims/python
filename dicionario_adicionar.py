'''
Dicionário
Os dicionários são usados ​​para armazenar valores de dados em pares chave: valor.

Um dicionário é uma coleção ordenada *, modificável e não permite duplicatas.
'''

# Os dicionários são escritos com chaves e têm chaves e valores:
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
print(thisdict)

'''
Itens de dicionário
Os itens do dicionário são ordenados, alteráveis ​​e não permitem duplicatas.

Os itens do dicionário são apresentados em pares chave: valor e podem ser referenciados usando o nome da chave.

Exemplo
Imprima o valor da "marca" do dicionário:
'''

thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
print(thisdict["brand"])

